package com.yedam.java.quiz;

public class Quiz1 {

	public static void main(String[] args) {
		int a = 10, b = 50, c = 90;
		
		int temp = a;
		a = b;
		b = c;
		c = temp;
		System.out.println("a= " + a);
		System.out.println("b= " + b);
		System.out.println("c= " + c);
		
		System.out.println("a = " + a + " , b = " + b + " , c = " + c);
		
		
	}

}
